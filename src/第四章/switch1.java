package 第四章;

public class switch1 {
    
    public static void main(String args[]){
        int a = 33 ;

        switch (a) {
            case 90:
                System.out.println("a=90");
                break;
            case 100:
                System.out.println("a = 100");
            default:
                break;
        }
    }

    
}
